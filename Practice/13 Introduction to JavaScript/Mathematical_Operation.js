// Addition Operation
var onionPrice = 20;
var eggPrice = 10;
var totalPrice = onionPrice + eggPrice;
console.log(totalPrice); // 30

// short hand
var price = 10;
// price = price + 2
price += 2;
console.log(price) // 12



// Subtraction Operation
var onionPrice = 20;
var eggPrice = 10;
var totalPrice = onionPrice - eggPrice;
console.log(totalPrice); // 10



// Multiplication Operation
var onionPrice = 20;
var eggPrice = 10;
var totalPrice = onionPrice * eggPrice;
console.log(totalPrice); // 200



// Division Operation
var onionPrice = 20;
var eggPrice = 10;
var totalPrice = onionPrice / eggPrice;
console.log(totalPrice); // 2



// Modulus Operation
var onionPrice = 5;
var eggPrice = 2;
var totalPrice = onionPrice % eggPrice;
console.log(totalPrice); // 1