// Find all the odd numbers from 61 to 100.
let num = 61;

while (num <= 100){
    if ( num % 2 !== 0 ){
        console.log(num);   // 64, 63, ....
    }
    num++
}


// Find all the even numbers from 78 to 98.

let number = 78;

while (number <= 98){
    console.log(number);    // 78, 80, ....
    number = number + 2;
}